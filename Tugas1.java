public class Tugas1{
    public static void main(String[] args) {
        int nilaiA = 3;
        int nilaiB = 5;
        int hargaX = nilaiA + nilaiB;
        int Y = 0;

        if(hargaX > 0){
            Y = nilaiA * nilaiB;
        }else{
            Y = hargaX;
        }

        System.out.println(nilaiA);
        System.out.println(nilaiB);
        System.out.println(hargaX);
        System.out.println(Y);
    }
}